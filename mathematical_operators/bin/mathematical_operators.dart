void main(List<String> arguments) {
  double? penjumlahan, pengurangan, perkalian, pembagian1, mod;
  int? pembagian2;

  // penjumlahan
  penjumlahan = 19 + 7;
  print("hasil : $penjumlahan");

  // pengurangan
  pengurangan = 19 - 7;
  print("hasil : $pengurangan");

  // perkalian
  perkalian = 19 * 7;
  print("hasil : $perkalian");

  // pembagian1
  pembagian1 = 19 / 7;
  print("hasil : $pembagian1");

  // pembagian2
  pembagian2 = 19 ~/ 7;
  print("hasil : $pembagian2");

  // modulus
  mod = 19 % 7;
  print("hasil : $mod");
}
